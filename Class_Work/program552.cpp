/////////////////////////////////////////////////////////////////
//
//  Function Name : IsFileExist
//  Description :   It is used to check whether file is already existing or not
//  Input :         It accepts file name 
//  output :        It returns true or fasle
//  Author :        Om Satish Mahale
//  Date :          16/01/2026
//
/////////////////////////////////////////////////////////////////
bool IsFileExist(
                    char *name  //File Name
)
{
    PINODE temp = head;
    bool bFlag=false;

    while(temp != NULL)
    {
        if(strcmp(name,temp->FileName) == 0 &&(temp->FileType == REGULARFILE))
        {
            bFlag = true;
            break;
        }
        temp=temp->next;
    }
    return bFlag;
}

/////////////////////////////////////////////////////////////////
//
//  Function Name : CreateFile
//  Description :   It is used to create the new regular file
//  Input :         It accepts file name and permissions
//  output :        It returns file descriptor
//  Author :        Om Satish Mahale
//  Date :          16/01/2026
//
/////////////////////////////////////////////////////////////////
int CreateFile(char *name,         //name of new file
                int permission     //Permission for that file
            )
{
    PINODE temp= head;
    int i=0;
    printf("Total number of Inodes remaining : %d\n",superobj.FreeInodes);
    
    //If name is missing
    if(name == NULL)
    {
        return ERR_INVALID_PARAMETER;
    }

    //If permission value is wrong
    //permission = 1 -> READ
    //permisssion = 2 -> WRITE
    //permission = 3 -> READ + WRITE
    if(permission < 1 || permission > 3)
    {
        return ERR_INVALID_PARAMETER;
    }

    //If the Inodes are full
    if(superobj.FreeInodes == 0)
    {
        return ERR_NO_INODES;
    }

    //If file is already present
    if(IsFileExist(name)== true)
    {
        return ERR_FILE_ALREADY_EXIST;
    }

    // Search empty Inode
    while(temp != NULL)
    {
        if(temp->FileType == 0)
        {
            break;
        }
        temp=temp->next;
    }

    if(temp == NULL)
    {
        printf("There is no inode\n");
        return ERR_NO_INODES;
    }

    //Note : 0,1 and 2 are reserved 
    //Search for empty UFDT entry
    for(i=3;i<MAXOPENFILES;i++)
    {
        if(uareaobj.UFDT[i]==NULL)
        {
            break;
        }
    }

    //UFDT is full
    if(i == MAXOPENFILES)
    {
        return ERR_MAX_FILES_OPEN;
    }

    //Allocate memory for file table 
    uareaobj.UFDT[i] = (PFILETABLE)malloc(sizeof(FILETABLE));

    //Initialise File table
    uareaobj.UFDT[i]->ReadOffset=0;
    uareaobj.UFDT[i]->WriteOffset=0;
    uareaobj.UFDT[i]->Mode=permission;

    //Connect file table with Inode
    uareaobj.UFDT[i]->ptrinode =temp;

    //Initialise elements of Inode
    strcpy(uareaobj.UFDT[i]->ptrinode->FileName,name);
    uareaobj.UFDT[i]->ptrinode->FileSize = MAXFILESIZE;
    uareaobj.UFDT[i]->ptrinode->ActualFileSize = 0;
    uareaobj.UFDT[i]->ptrinode->FileType = REGULARFILE;
    uareaobj.UFDT[i]->ptrinode->ReferenceCount = 1;
    uareaobj.UFDT[i]->ptrinode->Permission = permission;

    //Allocate memory for files data
    uareaobj.UFDT[i]->ptrinode->Buffer = (char *)malloc(MAXFILESIZE);

    superobj.FreeInodes--;

    return i;
}

